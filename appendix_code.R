#' 
#' The steep declines through k = 2-5 flatten out sharply from **k = 6 onward** -- the elbow sits
#' at k = 5 or 6.
#' 
#' ## K-means -- (b) Silhouette method
#' 
#' Computed on a random 8,000-customer sample (silhouette is O(n^2), infeasible on the full
#' 100,000 rows); cluster assignments themselves come from k-means fit on the **full** dataset.
#' 
## ----kmeans-silhouette, cache=TRUE--------------------------------------------
set.seed(602)
samp_idx <- sample(nrow(X), 8000)
Xs <- X[samp_idx, ]

sil_width <- sapply(2:10, function(k) {
  km <- kmeans(X, centers = k, nstart = 10, iter.max = 50)
  km_labels_sample <- km$cluster[samp_idx]
  ss <- silhouette(km_labels_sample, dist(Xs))
  mean(ss[, 3])
})
sil_df <- data.frame(k = 2:10, avg_silhouette = round(sil_width, 4))
print(sil_df)

#' 
## ----silhouette-plot----------------------------------------------------------
ggplot(sil_df, aes(k, avg_silhouette)) +
  geom_line(color = "#C24D2C", linewidth = 1) +
  geom_point(size = 2, color = "#C24D2C") +
  geom_vline(xintercept = 6, linetype = "dashed", color = "grey40") +
  scale_x_continuous(breaks = 2:10) +
  labs(title = "K-means Silhouette Method", x = "Number of clusters (k)",
       y = "Average silhouette width (sample n=8,000)") +
  theme_minimal(base_size = 13)

#' 
#' The global maximum is at k = 2 (0.293), but there is a clear local peak at **k = 6-7**
#' (0.286-0.288), after a dip at k = 3.
#' 
#' ## (c) Reconciling elbow vs. silhouette
#' 
#' k = 2 is statistically the tightest split but merely separates "low debt" from "high debt /
#' elevated DTI" customers -- too coarse for risk-based segmentation. **k = 6** sits at a local
#' silhouette peak nearly as high as the global maximum, and is exactly where the elbow's marginal
#' WSS gains flatten out. Profiling confirms k = 6 is business-meaningful, isolating a distinct
#' high-risk cluster. **Final choice: k = 6.**
#' 
## ----kmeans-final-------------------------------------------------------------
set.seed(602)
km6 <- kmeans(X, centers = 6, nstart = 10, iter.max = 50)
cat("Cluster sizes:\n")
print(table(km6$cluster))
cat("\nCluster centers (scaled features):\n")
print(round(km6$centers, 2))

#' 
#' ## Second clustering method: DBSCAN
#' 
#' **Why it complements K-means:** K-means assumes spherical, similarly-sized clusters and forces
#' every customer into one of k centroids -- genuine outliers get averaged into the nearest group,
#' diluting exactly the extreme-risk customers a risk-classification exercise needs to isolate.
#' DBSCAN makes no shape assumption and explicitly separates dense "normal" regions from sparse
#' outlier points, labelling the latter as **noise**.
#' 
#' `eps` was chosen from the k-distance elbow (minPts = 2 x dimensionality = 18):
#' 
## ----dbscan-eps-selection, cache=TRUE-----------------------------------------
minPts <- 2 * ncol(X)
kdist <- sort(kNNdist(X, k = minPts))
qs <- seq(0.85, 0.99, by = 0.02)
print(round(quantile(kdist, qs), 3))

#' 
## ----dbscan-final, cache=TRUE-------------------------------------------------
db <- dbscan(X, eps = 0.60, minPts = minPts)
cat("DBSCAN cluster sizes (0 = noise):\n")
print(table(db$cluster))

#' 
#' **Advantage for this data:** comparing DBSCAN's noise points against clustered points on the
#' raw (unscaled) risk variables shows the noise set is dramatically worse on every indicator
#' simultaneously -- DBSCAN surfaced this structure automatically, without being told to look for
#' "risky" customers.
#' 
## ----dbscan-profile-----------------------------------------------------------
noise_flag <- db$cluster == 0
compare_vars <- c("debt_to_income_ratio","total_outstanding_debt","num_missed_payments",
                   "overdraft_usage_yearly","credit_score","monthly_income")
for (v in compare_vars) {
  cat(sprintf("%-25s noise mean=%10.2f | clustered mean=%10.2f\n",
              v, mean(df[[v]][noise_flag]), mean(df[[v]][!noise_flag])))
}

#' 
#' ## Visual comparison (PCA projection)
#' 
## ----pca-plots, fig.width=8, fig.height=6-------------------------------------
set.seed(602)
pca <- prcomp(X, center = FALSE, scale. = FALSE)
var_exp <- round(100 * (pca$sdev^2 / sum(pca$sdev^2))[1:2], 1)

plot_df <- data.frame(PC1 = pca$x[,1], PC2 = pca$x[,2],
                        kmeans_cluster = factor(km6$cluster),
                        dbscan_cluster = factor(ifelse(db$cluster == 0, "Noise", db$cluster)))
plot_samp <- plot_df[sample(nrow(plot_df), 12000), ]

ggplot(plot_samp, aes(PC1, PC2, color = kmeans_cluster)) +
  geom_point(alpha = 0.4, size = 0.8) +
  labs(title = "K-means (k=6) clusters -- PCA projection",
       x = paste0("PC1 (", var_exp[1], "%)"), y = paste0("PC2 (", var_exp[2], "%)"),
       color = "Cluster") +
  theme_minimal(base_size = 13)

ggplot(plot_samp, aes(PC1, PC2, color = dbscan_cluster)) +
  geom_point(alpha = 0.4, size = 0.8) +
  scale_color_manual(values = c("1"="#2C6E9E","2"="#C24D2C","3"="#3A9E4D",
                                  "4"="#8E5EA2","5"="#D4A017","Noise"="grey70")) +
  labs(title = "DBSCAN clusters -- PCA projection (grey = noise/outliers)",
       x = paste0("PC1 (", var_exp[1], "%)"), y = paste0("PC2 (", var_exp[2], "%)"),
       color = "Cluster") +
  theme_minimal(base_size = 13)

